# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def hasCycle(self, head):
        """
        :type head: ListNode
        :rtype: bool
        """
        if not head:
            return False
        while head.next and head.next.val is not None:
            head = head.next
            head.val = None
        if not head.next:
            return False
        else:
            return True
